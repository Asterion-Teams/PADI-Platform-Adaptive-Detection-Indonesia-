"""
Prepare Roboflow 'vehicle-detection v3' dataset for training.

Responsibilities:
  1. Creates a valid/ split from train/ (default 10% images moved, not copied,
     so we don't bloat disk). Use --copy to keep originals in train/.
  2. Rewrites data.yaml with absolute paths so YOLOv8 can pick it up.
  3. Sanity-checks every image has a matching label file.

Usage:
    python scripts/prepare_vehicle_dataset.py                # 10% valid split
    python scripts/prepare_vehicle_dataset.py --valid-pct 15 # different ratio
    python scripts/prepare_vehicle_dataset.py --copy         # copy instead of move
"""
from __future__ import annotations

import argparse
import os
import random
import shutil
import sys


DATASET_ROOT = os.path.abspath(
    os.path.join(os.path.dirname(__file__), "..", "models", "vehicle-detection.v3i.yolov8")
)


def ensure_dir(path: str) -> None:
    os.makedirs(path, exist_ok=True)


def list_images(img_dir: str):
    exts = (".jpg", ".jpeg", ".png", ".bmp")
    return sorted(f for f in os.listdir(img_dir) if f.lower().endswith(exts))


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--valid-pct", type=float, default=10.0,
                   help="Percentage of images to move to valid/ (default: 10)")
    p.add_argument("--copy", action="store_true",
                   help="Copy files instead of moving them (keeps train intact)")
    p.add_argument("--seed", type=int, default=42)
    p.add_argument("--dry-run", action="store_true")
    args = p.parse_args()

    if not os.path.isdir(DATASET_ROOT):
        print(f"[ERROR] Dataset root not found: {DATASET_ROOT}")
        return 1

    train_img = os.path.join(DATASET_ROOT, "train", "images")
    train_lbl = os.path.join(DATASET_ROOT, "train", "labels")
    valid_img = os.path.join(DATASET_ROOT, "valid", "images")
    valid_lbl = os.path.join(DATASET_ROOT, "valid", "labels")

    if not os.path.isdir(train_img):
        print(f"[ERROR] train/images missing: {train_img}")
        return 1

    images = list_images(train_img)
    print(f"[INFO] Found {len(images)} images in train/")

    # Sanity: every image should have a label.
    missing = []
    for img in images:
        stem = os.path.splitext(img)[0]
        lbl = os.path.join(train_lbl, stem + ".txt")
        if not os.path.isfile(lbl):
            missing.append(img)
    if missing:
        print(f"[WARN] {len(missing)} images without labels (first 5: {missing[:5]})")

    ensure_dir(valid_img)
    ensure_dir(valid_lbl)

    # If valid/ already has data, skip unless it's empty.
    existing_valid = list_images(valid_img)
    if existing_valid:
        print(f"[INFO] valid/ already has {len(existing_valid)} images. Skipping split.")
    else:
        rng = random.Random(args.seed)
        shuffled = list(images)
        rng.shuffle(shuffled)
        n_valid = max(1, int(round(len(shuffled) * (float(args.valid_pct) / 100.0))))
        valid_set = shuffled[:n_valid]
        print(f"[INFO] Selecting {n_valid} images for valid split ({args.valid_pct:.1f}%)")

        for i, img in enumerate(valid_set):
            stem = os.path.splitext(img)[0]
            src_img = os.path.join(train_img, img)
            src_lbl = os.path.join(train_lbl, stem + ".txt")
            dst_img = os.path.join(valid_img, img)
            dst_lbl = os.path.join(valid_lbl, stem + ".txt")
            if args.dry_run:
                continue
            if args.copy:
                shutil.copy2(src_img, dst_img)
                if os.path.isfile(src_lbl):
                    shutil.copy2(src_lbl, dst_lbl)
            else:
                shutil.move(src_img, dst_img)
                if os.path.isfile(src_lbl):
                    shutil.move(src_lbl, dst_lbl)
            if (i + 1) % 200 == 0:
                print(f"  ...{i+1}/{n_valid}")
        if args.dry_run:
            print("[DRY RUN] No files moved.")

    # Rewrite data.yaml with absolute paths so ultralytics can resolve from anywhere
    data_yaml = os.path.join(DATASET_ROOT, "data.yaml")
    content = (
        f"# Auto-generated by prepare_vehicle_dataset.py\n"
        f"path: {DATASET_ROOT.replace(os.sep, '/')}\n"
        f"train: train/images\n"
        f"val: valid/images\n"
        f"\n"
        f"nc: 6\n"
        f"names: ['bus', 'car', 'microbus', 'motorbike', 'pickup-van', 'truck']\n"
        f"\n"
        f"roboflow:\n"
        f"  workspace: lynkeus03\n"
        f"  project: vehicle-detection-by9xs\n"
        f"  version: 3\n"
        f"  license: CC BY 4.0\n"
    )
    if not args.dry_run:
        with open(data_yaml, "w", encoding="utf-8") as f:
            f.write(content)
        print(f"[INFO] Wrote {data_yaml}")
    else:
        print(f"[DRY RUN] Would write: {data_yaml}")

    # Final summary
    n_train = len(list_images(train_img))
    n_valid = len(list_images(valid_img))
    print(f"\n[OK] Final split: train={n_train}, valid={n_valid}, total={n_train + n_valid}")
    print(f"[OK] data.yaml path: {data_yaml}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
